package com.wepeepzcreation.spacesaver;

public class UserProfile {

    public String Mobile;
    public String Email;
    public String Name;
    public String Password;

    public UserProfile (String userMobie, String userEmail, String userName, String userPassword){

        this.Mobile = userMobie;
        this.Email = userEmail;
        this.Name = userName;
        this.Password = userPassword;

    }


}